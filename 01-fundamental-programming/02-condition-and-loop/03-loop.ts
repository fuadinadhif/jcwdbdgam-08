/* -------------------------------- FOR LOOP -------------------------------- */
for (let i = 0; i < 3; i++) {
  console.log("Hai");
}

// LOOP 1
// i = 0
// i < 3 => 0 < 3 = true => jalankan kode yang ada di dalam loop
// i++ => i = i + 1 => i = 0 + 1 = 1

// LOOP 2
// i = 1
// i < 3 => 1 < 3 = true => jalankan kode yang ada di dalam loop
// i++ => i = i + 1 => i = 1 + 1 = 2

// LOOP 3
// i = 2
// i < 3 => 2 < 3 = true => jalankan kode yang ada di dalam loop
// i++ => i = i + 1 => i = 2 + 1 = 3

// LOOP 4
// i = 3
// i < 3 => 3 < 3 = false => hentikan loopnya

