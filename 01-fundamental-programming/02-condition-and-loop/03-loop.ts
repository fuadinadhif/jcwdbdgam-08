/* ------------------------------- WHILE LOOP ------------------------------- */
let i = 0;
while (i < 3) {
  console.log("Hai");
  i++;
}

/* ------------------------------ DO WHILE LOOP ----------------------------- */
let k = 0;
do {
  console.log("Hai");
  k++;
} while (k < 3);
